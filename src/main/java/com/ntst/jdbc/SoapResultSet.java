    private void parseSoapResponse(String rawResponse) throws SQLException {
        try {
            // 1. Extract the CDATA payload
            Matcher matcher = Pattern.compile("<!\\[CDATA\\[(.*?)]]>", Pattern.DOTALL).matcher(rawResponse);

            if (!matcher.find()) {
                // UPDATE THIS LINE: Throw the raw XML so DBeaver shows us the server's exact error message!
                throw new SQLException("No CDATA block found. Server returned: \n" + rawResponse);
            }

            String cdataXml = matcher.group(1).trim();

            // 2. Parse the inner XML using native Java DOM
            DocumentBuilderFactory factory = DocumentBuilderFactory.newInstance();
            DocumentBuilder builder = factory.newDocumentBuilder();
            Document doc = builder.parse(new ByteArrayInputStream(cdataXml.getBytes()));

            // 3. Extract Column Names
            NodeList colNodes = doc.getElementsByTagName("cols").item(0).getChildNodes();
            for (int i = 0; i < colNodes.getLength(); i++) {
                Node node = colNodes.item(i);
                if (node.getNodeType() == Node.ELEMENT_NODE) {
                    columnNames.add(((Element) node).getAttribute("tit"));
                }
            }

            // 4. Extract Row Data
            NodeList rowNodes = doc.getElementsByTagName("row");
            for (int i = 0; i < rowNodes.getLength(); i++) {
                Node rowNode = rowNodes.item(i);
                if (rowNode.getNodeType() == Node.ELEMENT_NODE) {
                    Map<String, String> rowData = new HashMap<>();
                    NodeList cellNodes = ((Element) rowNode).getElementsByTagName("col");
                    
                    for (int j = 0; j < cellNodes.getLength(); j++) {
                        if (j < columnNames.size()) {
                            String cellValue = ((Element) cellNodes.item(j)).getAttribute("val");
                            rowData.put(columnNames.get(j), cellValue);
                        }
                    }
                    rows.add(rowData);
                }
            }
        } catch (Exception e) {
            throw new SQLException("Failed to parse SOAP response XML: " + e.getMessage(), e);
        }
    }

    @Override
    public boolean next() throws SQLException {
        if (isClosed) throw new SQLException("ResultSet is closed.");
        currentRowIndex++;
        return currentRowIndex < rows.size();
    }

    @Override
    public String getString(int columnIndex) throws SQLException {
        if (isClosed) throw new SQLException("ResultSet is closed.");
        String colName = columnNames.get(columnIndex - 1);
        String val = rows.get(currentRowIndex).get(colName);

        this.wasLastValueNull = (val == null); // Track it!
        return val;
    }

